// Función helper para obtener ejemplos por tipo
export const getExamplesByType = (type, category) => {
  if (type === 'tone') {
    return toneExamples[category] || null
  } else if (type === 'objective') {
    return objectiveExamples[category] || null
  }
  return null
}

// Función para obtener todos los ejemplos de un tipo
export const getAllExamplesByType = (type) => {
  if (type === 'tone') {
    return toneExamples
  } else if (type === 'objective') {
    return objectiveExamples
  }
  return {}
}
